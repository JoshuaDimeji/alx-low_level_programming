#include <stdio.h>
/**
 * add - function to add two integers
 * sub - function to subtract two integers
 * mul - function to multiply two integers
 * div - function to divide two integers
 * mod - function to find the remainder when you divide two integers
 * @a: The first integer
 * @b: The second integer
 * Return: the value
 */

int add(int a, int b)
{
	return (a + b);
}


int sub(int a, int b)
{
	return (a - b);
}


int mul(int a, int b)
{
	return (a * b);
}


int div(int a, int b)
{
	return (a / b);
}


int mod(int a, int b)
{
	return (a % b);
}
